/*
  status bits will be saved in ADDR_FLASH_PAGE_0 --> 0x08000'0000 -> 0x08000'00FF
  0x000 - 0x0FF
  256 Bytes
  
*/
boolean checkValidAddr(uint32_t addr){
  if(addr > 0x087FF00)
    return false;
  else
    return true;
}

/*
*   toggle the bit value of the argument.
*   addr - the real address of the bit to be toggled
*   mask - the general location of the bit to toggle
*
*/
boolean toggleBit(uint8_t* addr, uint8_t mask){


    if(!checkValidAddr(addr))
      return false;

    int bit1 = addr & (1 << mask);
    int bit2;

    addr ^= 1 << mask;

    // check if sucessful
    bit2 = addr & (1 << mask);
    if(bit1 == bit2)
      return false;
    else
        return true;
}

/*
*   set bit
*   addr - the real address of the bit to be toggled
*   mask - the general location of the bit to toggle
*
*/
boolean setBit(uint8_t* addr, uint8_t mask){

    if(!checkValidAddr(addr))
      return false;

    int bit1 = addr & (1 << mask);
    int bit2;

    addr |= 1 << mask;

    // check if sucessful
    bit2 = addr & (1 << mask);
    if(bit1 == bit2)
      return false;
    else
        return true;
}

/*
*   clear bit
*   addr - the real address of the bit to be toggled
*   mask - the general location of the bit to toggle
*
*/
boolean clearBit(uint8_t* addr, uint8_t mask){

    if(!checkValidAddr(addr))
      return false;

    int bit1 = addr & (1 << mask);
    int bit2;

    addr &= ~(1 << mask);

    // check if sucessful
    bit2 = addr & (1 << mask);
    if(bit1 == bit2)
      return false;
    else
        return true;
}

